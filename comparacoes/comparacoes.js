// == comparação implícita

const numero = 5;
const texto = "5";

console.log(numero == texto); // retorna true

console.log(numero === texto); // retorna false

// typeof

console.log(typeof numero); // retorna number
console.log(typeof texto); // retorna string

// == compara o valor
// === compara o tipo

// conversão explicita

Number();
String();
